/**
 * Validate Iranian national code (کد ملی) using the official algorithm.
 */
export function validateNationalId(code: string): boolean {
  if (!code || code.length !== 10) return false;
  if (!/^\d{10}$/.test(code)) return false;

  const digits = code.split("").map(Number);

  // All identical digits are invalid
  if (new Set(digits).size === 1) return false;

  const checksum = digits[9];
  let sum = 0;
  for (let i = 0; i < 9; i++) {
    sum += digits[i] * (10 - i);
  }
  const remainder = sum % 11;
  const expected = remainder < 2 ? remainder : 11 - remainder;

  return expected === checksum;
}

/**
 * Validate Iranian mobile number (11 digits starting with 09).
 */
export function validateMobile(phone: string): boolean {
  return /^09\d{9}$/.test(phone);
}

/**
 * Validate Iranian postal code (10 digits).
 */
export function validatePostalCode(code: string): boolean {
  return /^\d{10}$/.test(code);
}

/**
 * Validate Persian (Farsi) name — allows Persian/Arabic unicode letters, spaces, and zero-width non-joiner.
 */
export function validatePersianName(name: string): boolean {
  return /^[\u0600-\u06FF\s\u200c]+$/.test(name.trim());
}

