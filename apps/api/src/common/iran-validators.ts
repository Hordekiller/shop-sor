/**
 * Check if province and city are valid in Iran.
 */
export function isValidIranLocation(province: string, city: string): boolean {
  const p = IRAN_PROVINCES.find((p) => p.name === province);
  if (!p) return false;
  return p.cities.includes(city);
}
